# Python은 4가지의 Scalar 타입이 있습니다. 
# int(정수), float(실수), None(값없음), bool(True, False) 4가지 입니다.

print (10)
print (0b10) #0b는 'bit'  2진수
print (0o10) #0o는 'octo' 8진수
print (0x10) #0x는 'hexa' 16진수

print (int(3.5))      #3
print (int(-3.5))     #-3
print (int(True) )    #1
print (int(False) )   #0
print (int("500"))    #500
print (int("1000",3)) #1000을 3진수로 반환

